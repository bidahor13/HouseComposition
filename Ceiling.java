public class Ceiling {

    private int height;
    private int paintColor;

    public Ceiling(int height, int paintColor) {
        this.height = height;
        this.paintColor = paintColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPaintColor() {
        return paintColor;
    }
}
